// Main dashboard function
async function showDashboard() {
  try {
    const metrics = await loadMonitoringData();
    const alerts = await loadRecentAlerts();
    const dashboard = generateDashboard(metrics, alerts);

    // Clear screen and show dashboard
    console.clear();
    console.log(dashboard);
  } catch (error) {
    console.error('Failed to display dashboard:', error.message);
    console.log('\nRun "npm run ralph:monitor check" to generate initial monitoring data.');
  }
}

// CLI interface
async function main() {
  const args = process.argv.slice(2);
  const command = args[0];

  switch (command) {
    case 'refresh':
    case undefined:
      await showDashboard();
      break;

    case 'watch':
      console.log('ðŸ‘€ Starting dashboard auto-refresh (updates every 30 seconds)...');
      console.log('Press Ctrl+C to exit.\n');

      // Initial display
      await showDashboard();

      // Set up refresh interval
      setInterval(async () => {
        await showDashboard();
      }, 30000); // Refresh every 30 seconds
      break;

    default:
      console.log(`
Usage: node scripts/ralph-queue-dashboard.mjs [command]

Commands:
  (none)     - Display current dashboard
  refresh     - Refresh dashboard data
  watch       - Auto-refresh dashboard every 30 seconds

Examples:
  npm run ralph:dashboard
  npm run ralph:dashboard watch
      `);
      process.exit(1);
  }
}

// Handle graceful shutdown
process.on('SIGINT', () => {
  console.log('\nðŸ‘‹ Shutting down Ralph TUI dashboard...');
  process.exit(0);
});

process.on('SIGTERM', () => {
  console.log('\nðŸ‘‹ Shutting down Ralph TUI dashboard...');
  process.exit(0);
});

// Run the dashboard
if (import.meta.url === `file://${process.argv[1]}`) {
  main().catch(console.error);
}
